window.getModelsByBrand=function(brand){

return window.mobileDatabase[brand.toLowerCase()]||[];

};

window.searchModels=function(keyword){

const results=[];

if(!keyword) return results;

const query=keyword.toLowerCase();

Object.entries(window.mobileDatabase).forEach(([brand,models])=>{

models.forEach(model=>{

if(

model.toLowerCase().includes(query) ||

brand.toLowerCase().includes(query)

){

results.push({

brand,

model

});

}

});

});

return results;

};

